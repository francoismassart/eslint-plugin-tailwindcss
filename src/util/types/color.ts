// RGB %: rgb(25%,50%,75%)
const RGBPercentages = 'rgb\\(\\d{1,3}%\\,\\d{1,3}%\\,\\d{1,3}%\\)';

// RGBA: rgba(50,100,255,.5), rgba(50,100,255,50%)
