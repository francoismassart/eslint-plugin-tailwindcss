// RGB[A] hexa: #123456AA, #B4DA55, #000A, #123
const hexRGBA = '\\#(([0-9A-Fa-f]{8})|([0-9A-Fa-f]{6})|([0-9A-Fa-f]{4})|([0-9A-Fa-f]{3}))';

// RGB 0-255: rgb(10,20,30)
